package cmabreu.spectral.entity;

public class Fragment {
	private String serial;
	private String status;
	private String totalInstances;
	private String remainingInstances;
	private String activities;
	
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTotalInstances() {
		return totalInstances;
	}
	public void setTotalInstances(String totalInstances) {
		this.totalInstances = totalInstances;
	}
	public String getRemainingInstances() {
		return remainingInstances;
	}
	public void setRemainingInstances(String remainingInstances) {
		this.remainingInstances = remainingInstances;
	}
	public String getActivities() {
		return activities;
	}
	public void setActivities(String activities) {
		this.activities = activities;
	}
	
}
